name = "sid sen"

print(len(name)) # Returns the length of the string

print(name.endswith("du")) # Checks if the string ends with the argument

print(name.startswith("Sid")) # Checks if the string starts with the argument

print(name.capitalize()) # Capitalizes the first letter of the string

print(name.upper()) # Converts the string to upper case

print(name.lower()) # Converts the string to lower case

print(name.find("d")) # Returns the index of the first occurence of the argument

print(name.replace("dd","sh")) # Replaces the first argument with the second argument

print(name.isalpha())   # Checks if the string contains only alphabets

print(name.isalnum()) # Checks if the string contains only alphabets and numbers

print(name.isdigit()) # Checks if the string contains only digits

print(name.islower()) # Checks if all the characters are in lower case

print(name.isupper()) # Checks if all the characters are in upper case

print(name.upper().isupper()) # Checks if all the characters are in upper case

print(name.lower().islower()) # Checks if all the characters are in lower case

print(name.isspace()) # Checks if the string contains only spaces

print(name.istitle()) # Checks if the string is in title case

print(name.title()) # Capitalizes the first letter of each word

print(name.swapcase()) # Swaps the case of the string

print(name.count("d")) # Counts the number of occurences of the argument

print(name.split("d")) # Splits the string at the argument

print(name.join("d")) # Joins the string with the argument

